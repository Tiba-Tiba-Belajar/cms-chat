                  <path d="M12.545,4.224h1.15V7.55C15.279,6.46,16.895,5.822,18.66,5.822c2.782,0,5.182,1.016,6.926,2.751l-2.911,2.88c-1.233-1.194-2.791-1.925-4.536-1.925c-3.058,0-5.556,2.496-5.556,5.573s2.498,5.573,5.556,5.573c2.268,0,4.226-1.357,5.09-3.311h-5.09v-3.773h8.727c0.189,0.835,0.293,1.7,0.293,2.593c0,6.263-4.958,11.206-11.14,11.206c-6.21,0-11.267-5.27-11.267-11.778S6.335,4.224,12.545,4.224z" fill="#FBBC05" />
